  function get_review_avatar_pool(string $type = 'majice'): array {
    $type = ($type === 'boxeri' || $type === 'bokserice') ? 'bokserice' : 'majice';

    $dir_path = trailingslashit(get_stylesheet_directory()) . 'auto_reviews/' . $type . '-slike/';
